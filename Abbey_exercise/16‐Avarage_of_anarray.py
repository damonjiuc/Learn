mas = ('961 1570 1822 1530 1099 517 775 1425 534 991 421 0', '385 341 241 127 360 343 466 330 287 337 3 386 219 191 221 0', '227 105 26 109 217 0', '159 215 144 147 88 129 0', '472 212 363 322 218 119 305 225 0', '269 431 233 90 187 426 264 356 408 339 45 416 152 0', '4706 5244 9774 3265 3635 5903 7953 15123 3709 2011 16195 0', '23 90 92 170 82 216 167 3 147 203 183 159 119 97 0', '1207 1515 1825 1290 788 1076 483 0')
for i in range(n):
    splited = mas[i].split(' ')
    inted = []
    sum = 0
    for i in range(len(splited)):
        inted.append(int(splited[i]))
    for i in range(len(inted) - 1):
        sum += inted[i]
    res = sum / (len(inted) - 1)
    if res % 1 < 0.5:
        print(int(res // 1), end=' ')
    else:
        print(int(res // 1 + 1), end=' ')
    
